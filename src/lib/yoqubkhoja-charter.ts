export function resolveCharterDocument(
  content: OrganizationSectionContent | undefined,
  organizationId: string,
  organization?: Organization
): CharterDocument | null {
  if (content?.charterDocument) return content.charterDocument;
  if (organizationId !== YOQUBKHOJA_INNOVATION_CENTER_ID) return null;
  return buildDefaultYoqubkhojaCharter({
    founder: organization?.director,
    address: organization?.address,
    location: organization?.address,
  });
}
